package org.cang24.tests.changemaking.ui;

public class MenuOption {
	int index;
	String description;
	
	public MenuOption(int index, String description) {
		super();
		this.index = index;
		this.description = description;
	}
	public int getIndex() {
		return index;
	}
	public String getDescription() {
		return description;
	}
}
